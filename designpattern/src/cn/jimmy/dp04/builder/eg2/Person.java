package cn.jimmy.dp04.builder.eg2;

public class Person {
  private String head;
  private String body;
  private String foot;
  
  public void setHead(String head) {
    this.head = head;
  }
  
  public String getHead() {
    return this.head;
  }

  public void setBody(String body) {
    this.body = body;
  }
  
  public String getBody(){
    return this.body;
  }
  
  public void setFoot(String foot) {
    this.foot = foot;
  }
  
  public String getFoot() {
    return this.foot;
  }
}
